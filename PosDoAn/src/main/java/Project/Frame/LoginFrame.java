    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_loginButtonActionPerformed
        // đăng nhập
            // Lấy thông tin đăng nhập từ các trường nhập liệu
            String employeeID = EmployeeIDTextField.getText().trim();
            String password = passwordTextField1.getText().trim();

            // Kiểm tra các trường không được để trống
            if (employeeID.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(
                        null,
                        "Vui lòng nhập đầy đủ thông tin",
                        "Cảnh báo",
                        JOptionPane.WARNING_MESSAGE
                );
                return;  // Dừng xử lý nếu thiếu thông tin
            }

            try {
                // Kiểm tra thông tin đăng nhập
                if (checkIfExist(employeeID, password)) {
                    // Kiểm tra nếu là quản lý
                    if (checkifManager(employeeID)) {
                        new HomeFrameManager().setVisible(true);
                        this.dispose();
                    }
                    else {
                        // Mở giao diện cho nhân viên thông thường
                        new HomeFrameStaff().setVisible(true);
                        this.dispose();
                    }
                }
                else {
                    JOptionPane.showMessageDialog(
                            null,
                            "Sai mã nhân viên hoặc mật khẩu",
                            "Lỗi đăng nhập",
                            JOptionPane.ERROR_MESSAGE
                    );
                }
            } catch (SQLException ex) {
                // Xử lý lỗi kết nối cơ sở dữ liệu
                JOptionPane.showMessageDialog(
                        null,
                        "Lỗi kết nối cơ sở dữ liệu: " + ex.getMessage(),
                        "Lỗi hệ thống",
                        JOptionPane.ERROR_MESSAGE
                );
                ex.printStackTrace();
            }
    }//GEN-LAST:event_loginButtonActionPerformed

