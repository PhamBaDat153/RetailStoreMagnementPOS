    private Boolean checkIfExist(String employeeID, String password) throws SQLException {
        //Kiểm tra xem nhân viên có tồn tại không
        String sql = "SELECT COUNT(*) FROM employee WHERE employee_ID = ? AND password = ?";

        try (Connection conn = DatabaseConnector.connectDatabase();
             PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setString(1, employeeID);
            stmt.setString(2, password);

            try (ResultSet rs = stmt.executeQuery()) {
                if (rs.next()) {
                    int count = rs.getInt(1);
                    return count > 0;
                }
            }
        }
        return false;
    }

    private boolean checkifManager(String employeeID) throws SQLException {
        // Kiểm tra nhân viên có phải là quản lý
        String sql = "SELECT COUNT(*) FROM employee WHERE employee_ID = ? AND position = 'Quản lý'";

        try (Connection conn = DatabaseConnector.connectDatabase();
             PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setString(1, employeeID);

            try (ResultSet rs = stmt.executeQuery()) {
                return rs.next() && rs.getInt(1) > 0;
            }
        }
    }

